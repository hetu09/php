<?php
// switch case
    $num1=10;
    $num2=20;
    $ans = '-';

    switch ($ans) {
        case '+':
            echo "Addition of num1 and num2 :....".($num1+$num2)."<br>";
            break;

         case '-':
            echo "Addition of num1 and num2 :....".($num1-$num2)."<br>";
            break;
        
        default:
            echo "Invalid choice";
            break;
    }
?>